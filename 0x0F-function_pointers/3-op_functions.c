#include "3-calc.h"
/**
 * op_add - sum of two numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - differnce of two numbers
 * @a: first number
 * @b: second number
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product of two numbers
 * @a: first number
 * @b: second number
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - quotient of a division
 * @a: first number
 * @b: seocnd number
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of a division
 * @a: first number
 * @b: seocnd number
 * Return: remainder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
